                        <h3><b>A basic definition of JavaScript </b></h3>
                        <br>
JavaScript is a programming language that is:
<br><br>

<ul>
    <li><b>high level</b>: it provides abstractions that allow you to ignore the details of the machine where it’s running on. It manages memory automatically with a garbage collector, so you can focus on the code instead of managing memory locations, and provides many constructs which allow you to deal with highly powerful variables and objects.
        </li><br>
    
    <li><b>dynamic</b>: opposed to static programming languages, a dynamic language executes at runtime many of the things that a static language does at compile time. This has pros and cons, and it gives us powerful features like dynamic typing, late binding, reflection, functional programming, object runtime alteration, closures and much more.</li><br>
    
    <li><b>dynamically typed</b>: a variable does not enforce a type. You can reassign any type to a variable, for example assigning an integer to a variable that holds a string.</li><br>
    
    <li><b>weakly typed</b>: as opposed to strong typing, weakly (or loosely) typed languages do not enforce the type of an object, allowing more flexibility but denying us type safety and type checking (something that TypeScript and Flow aim to improve)</li><br>
    
    <li><b>interpreted</b>: it’s commonly known as an interpreted language, which means that it does not need a compilation stage before a program can run, as opposed to C, Java or Go for example. In practice, browsers do compile JavaScript before executing it, for performance reasons, but this is transparent to you: there is no additional step involved.</li><br>
    
    <li><b>multi-paradigm</b>: the language does not enforce any particular programming paradigm, unlike Java for example which forces the use of object oriented programming, or C that forces imperative programming. You can write JavaScript using an object-oriented paradigm, using prototypes. You can write JavaScript in functional programming style, or even in an imperative style.</li><br>
    
    </ul>
