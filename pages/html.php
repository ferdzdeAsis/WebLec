						<h1><center>HTML</center></h1>
                        <br><br><br>
                        <b>HTML</b> (HyperText Markup Language) is the most basic building block of the Web. It defines the meaning and structure of web content. Other technologies besides HTML are generally used to describe a web page's functionality/behavior (JavaScript) or appearance/presentation<a href="css.html">(CSS).</a>
                        
                        <br>
                        <br>
                        "Hypertext" refers to links that connect web pages to one another, either within a single website or between websites. Links are a fundamental aspect of the Web. By uploading content to the Internet and linking it to pages created by other people, you become an active participant in the World Wide Web.
                        
                        <br>
                        <br>
                        HTML uses "markup" to annotate text, images, and other content for display in a Web browser. HTML markup includes special "elements" such as <br>
                        <center><b>
                            &lt;head&gt;, &lt;title&gt;, &lt;body&gt;, &lt;header&gt;, &lt;footer&gt;, <br><br> 
                            
                            &lt;article&gt;, &lt;section&gt;, &lt;p&gt;, &lt;div&gt;, &lt;span&gt;, <br><br>
                            
                            &lt;img&gt;, &lt;aside&gt;, &lt;audio&gt;, &lt;canvas&gt;, &lt;datalist&gt;, <br><br>
                            
                            &lt;details&gt;, &lt;embed&gt;, &lt;nav&gt;, &lt;output&gt;, &lt;progress&gt;, <br><br> 
                            
                            &lt;video&gt; </b> and many others.</center>
                        
                        <br>
                        <br>
                        An HTML element is set off from other text in a document by "tags", which consist of the element name surrounded by "<" and ">".  The name of an element inside a tag is case insensitive. That is, it can be written in uppercase, lowercase, or a mixture. For example, the <b>"&lt;title&gt;"</b> tag can be written as <b>&lt;Title&gt;</b>, <b>&lt;TITLE&gt;</b>, or in any other way.
                        
