                        The main parts of our element are as follows:
                        <br>
                        <br>
                        1. <b>The opening tag:</b> This consists of the name of the element, wrapped in opening and closing angle brackets. This states where the element begins or starts to take effect. <br><br>
                        2. <b>The closing tag:</b> This is the same as the opening tag, except that it includes a forward slash before the element name. This states where the element ends. Failing to add a closing tag is one of the standard beginner errors and can lead to strange results.<br><br>
                        3. <b>The content:</b> This is the content of the element, in most cases it is just a text.<br><rb></rb><br>
                        4. <b>The element:</b> The opening tag, the closing tag and the content together comprise the element.
